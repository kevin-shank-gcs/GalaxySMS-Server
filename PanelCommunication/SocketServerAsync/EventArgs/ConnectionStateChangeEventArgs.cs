using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using GalaxySMS.Business.Entities;

namespace GCS.PanelCommunication.PanelCommunicationServerAsync
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Additional information for connection state change events. </summary>
    ///
    /// <remarks>   Kevin, 12/26/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

	public class ConnectionStateChangeEventArgs : EventArgs
	{
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets information describing the connection. </summary>
        ///
        /// <value> Information describing the connection. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

		public CpuConnectionInfo ConnectionInfo { get; private set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Kevin, 12/26/2018. </remarks>
        ///
        /// <param name="connectionInfo">   Information describing the connection. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

		public ConnectionStateChangeEventArgs(CpuConnectionInfo connectionInfo)
		{
			ConnectionInfo = connectionInfo;
		}
	}
}
