using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace GCS.PanelCommunication.PanelCommunicationServerAsync
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Additional information for data received events. </summary>
    ///
    /// <remarks>   Kevin, 12/26/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

	public class DataReceivedEventArgs : EventArgs
	{
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the data. </summary>
        ///
        /// <value> The data. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

		public byte[] Data { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Kevin, 12/26/2018. </remarks>
        ///
        /// <param name="data"> [in,out] The data. </param>
        /// <param name="len">  The length. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

		public DataReceivedEventArgs(ref byte[] data, Int32 len)
		{
			Data = new byte[len];
			Array.Copy(data, 0, Data, 0, len);
		}
	}
}
