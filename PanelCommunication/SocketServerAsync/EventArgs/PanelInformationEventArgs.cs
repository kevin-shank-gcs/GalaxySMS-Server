using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using GalaxySMS.Business.Entities;

namespace GCS.PanelCommunication.PanelCommunicationServerAsync
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Additional information for panel information events. </summary>
    ///
    /// <remarks>   Kevin, 12/26/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

	public class PanelInformationEventArgs : EventArgs
	{
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets information describing the galaxy CPU. </summary>
        ///
        /// <value> Information describing the galaxy CPU. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

		public GalaxyCpuInformation GalaxyCpuInformation { get; private set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Kevin, 12/26/2018. </remarks>
        ///
        /// <param name="galaxyCpuInfo">    Information describing the galaxy CPU. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

		public PanelInformationEventArgs(GalaxyCpuInformation galaxyCpuInfo)
		{
			GalaxyCpuInformation = galaxyCpuInfo;
		}
	}
}
