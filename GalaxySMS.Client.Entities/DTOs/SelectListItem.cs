using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GalaxySMS.Client.Entities
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>
    /// Summary:
    ///     Represents the selected item in an instance of the System.Web.Mvc.SelectList class.
    /// </summary>
    ///
    /// <remarks>   Kevin, 12/26/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class SelectListItem
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Summary:
        ///     Initializes a new instance of the System.Web.Mvc.SelectListItem class.
        /// </summary>
        ///
        /// <remarks>   Kevin, 12/26/2018. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public SelectListItem()
        {

        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Summary:
        ///     Gets or sets a value that indicates whether this System.Web.Mvc.SelectListItem is
        ///     selected.
        /// 
        /// Returns:
        ///     true if the item is selected; otherwise, false.
        /// </summary>
        ///
        /// <value> True if selected, false if not. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public bool Selected { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Summary:
        ///     Gets or sets the text of the selected item.
        /// 
        /// Returns:
        ///     The text.
        /// </summary>
        ///
        /// <value> The text. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Text { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>
        /// Summary:
        ///     Gets or sets the value of the selected item.
        /// 
        /// Returns:
        ///     The value.
        /// </summary>
        ///
        /// <value> The value. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string Value { get; set; }
    }
}
