using System;
using System.Collections.Generic;
using System.ComponentModel;
using System.Linq;

namespace GCS.Core.Common.Core
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   Additional information for item property changed events. </summary>
    ///
    /// <remarks>   Kevin, 12/26/2018. </remarks>
    ///
    /// <typeparam name="T">    Generic type parameter. </typeparam>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    public class ItemPropertyChangedEventArgs<T> : System.EventArgs
           where T : INotifyPropertyChanged
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Kevin, 12/26/2018. </remarks>
        ///
        /// <param name="item">         The item. </param>
        /// <param name="propertyName"> The name of the property. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public ItemPropertyChangedEventArgs(T item, string propertyName)
        {
            Item = item;
            PropertyName = propertyName;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the item. </summary>
        ///
        /// <value> The item. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public T Item { get; set; }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Gets or sets the name of the property. </summary>
        ///
        /// <value> The name of the property. </value>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public string PropertyName { get; set; }
    }
}
